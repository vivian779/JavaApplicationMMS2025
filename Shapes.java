public class Shapes{
public static void main(String[] args) {
	
	// Box
	System.out.println("*****");
	System.out.println("*   *");
	System.out.println("*   *");
	System.out.println("*****");
	
	System.out.println();
	
	// Oval
	System.out.println("  ***  ");
	System.out.println(" *  *  ");
	System.out.println("*     *");
	System.out.println("  ***  ");
	
	System.out.println();
	
	// Arrow
	System.out.println("   *    ");
	System.out.println("  ***   ");
	System.out.println(" *****  ");
	System.out.println("   *    ");
	System.out.println("   *    ");
	
	System.out.println();
	
	// Diamond
	System.out.println("    *    ");
	System.out.println("  *  *  ");
	System.out.println("  *   * ");
	System.out.println("   * *  ");
	System.out.println("    *   ");
    }

    
}